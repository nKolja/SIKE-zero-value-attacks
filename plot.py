def plot_isogeny_tree(N, strategy, k, b_or_a):
    isog_prime = 3
    point_prime = 2
    if(b_or_a):
        isog_prime, point_prime = 2*point_prime, isog_prime
        k = (k - (k%2))/2
    fig = plt.figure(figsize=(30,30))
    plt.axis("off")
    o = pow(point_prime, k)
    # PLOT PATH TREE SKELET
    for c in range(N):
        plt.hlines(-c, 0, N-1-c, linestyles='solid', color='gray', linewidth=0.25)
        plt.text(-2 , -c - 0.15, str(c), color='gray', fontsize=5)
        plt.vlines(c, 0, -(N-1-c), linestyles='solid', color='gray', linewidth=0.25)
        plt.text(c-0.15, 0.5, str(c), color='gray', fontsize=5)

        plt.text(-4 , -c - 0.15, str(pow(isog_prime,c,o)), color='gray', fontsize=5)


    # PLOT TREE TRAVERSAL
    ii = 0
    pts_index = [0]*N
    npts = 0
    row = 0
    for column in range(1, N):
        while (row < N - column):
            m = strategy[ii]
            ii += 1
            pts_index[npts] = row
            npts += 1
            for mult in range(row, row+m):
                plt.arrow(column-1, -mult, 0, -1, linewidth = 1, head_width = 0.025, length_includes_head=True)            
            row += m
        row = pts_index[npts - 1]
        for isog in range(npts):
            plt.arrow(column-1, -pts_index[isog], 1, 0, linewidth = 1, head_width=0.025, length_includes_head=True)            
        npts -= 1


    # plt.show()
    fig.savefig('TREE.svg', format='svg', dpi=1200)
    plt.close(fig)




